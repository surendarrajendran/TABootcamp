package com.bootcampexercise.module5.activity;

/**
 * Class Name: Person_I
 *
 * Description:
 * This class represents a Person with attributes like name and age.
 */
public class Person_I {

    // Variable to store person's name
    private String name;

    // Variable to store person's age
    private int age;

    /**
     * Method to set person's name
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * Method to get person's name
     */
    public String getName() {
        return name;
    }

    /**
     * Method to set person's age
     */
    public void setAge(int age) {
        this.age = age;
    }

    /**
     * Method to get person's age
     */
    public int getAge() {
        return age;
    }
}