package com.bootcampexercise.module2.activity;

public class VariableAssignmentActivity {

    public static void main(String[] args) {

        // Variable to store an integer value (initialized with default value 0)
        int number = 0;

        // Updating the value of the variable
        number++;

        // Printing the updated value
        System.out.println("Updated value: " + number);
    }
}
